import { ref, onMounted, onUnmounted } from 'vue'
import { useRoute, useRouter } from 'vue-router'
import { useTabManager } from '../composables/useTabManager'

// Vue Router
const router = useRouter()
const route = useRoute()

// é¡µç­¾ç®¡ç†å™¨
const { addTab } = useTabManager()

// å“åº”å¼æ•°æ®
const isMobileMenuOpen = ref(false)
const isOnline = ref(navigator.onLine)
const isInstalled = ref(false)

// åˆ‡æ¢ç§»åŠ¨ç«¯èœå•
const toggleMobileMenu = () => {
  isMobileMenuOpen.value = !isMobileMenuOpen.value
  // é˜²æ­¢èƒŒæ™¯æ»šåŠ¨
  if (isMobileMenuOpen.value) {
    document.body.style.overflow = 'hidden'
  } else {
    document.body.style.overflow = 'auto'
  }
}

// å…³é—­ç§»åŠ¨ç«¯èœå•
const closeMobileMenu = () => {
  isMobileMenuOpen.value = false
  document.body.style.overflow = 'auto'
}

// å¤„ç†å¯¼èˆªç‚¹å‡»äº‹ä»¶
const handleNavClick = (path: string, title: string, icon: string) => {
  closeMobileMenu()
  
  // åˆ›å»ºæ¨¡æ‹Ÿè·¯ç”±å¯¹è±¡
  const mockRoute = {
    path,
    name: title,
    meta: {
      title,
      icon
    }
  }
  
  // æ·»åŠ åˆ°é¡µç­¾ç®¡ç†å™¨
  addTab(mockRoute, { setActive: true })
  
  // å¯¼èˆªåˆ°å¯¹åº”è·¯ç”±
  router.push(path)
}

// ç›‘å¬ç½‘ç»œçŠ¶æ€
const handleOnline = () => {
  isOnline.value = true
}

const handleOffline = () => {
  isOnline.value = false
}

// æ£€æµ‹PWAå®‰è£…çŠ¶æ€
const checkInstallStatus = () => {
  // æ£€æµ‹æ˜¯å¦åœ¨ç‹¬ç«‹æ¨¡å¼ä¸‹è¿è¡Œï¼ˆå·²å®‰è£…ï¼‰
  if (window.matchMedia('(display-mode: standalone)').matches) {
    isInstalled.value = true
  }
  
  // æ£€æµ‹æ˜¯å¦åœ¨PWAç¯å¢ƒä¸­
  if ('serviceWorker' in navigator) {
    navigator.serviceWorker.ready.then(() => {
      // Service Workerå·²å°±ç»ªï¼Œå¯èƒ½å·²å®‰è£…
    })
  }
}

// ç»„ä»¶æŒ‚è½½
onMounted(() => {
  // ç›‘å¬ç½‘ç»œçŠ¶æ€å˜åŒ–
  window.addEventListener('online', handleOnline)
  window.addEventListener('offline', handleOffline)
  
  // æ£€æŸ¥å®‰è£…çŠ¶æ€
  checkInstallStatus()
  
  // ç›‘å¬çª—å£å¤§å°å˜åŒ–ï¼Œè‡ªåŠ¨å…³é—­ç§»åŠ¨ç«¯èœå•
  const handleResize = () => {
    if (window.innerWidth > 768 && isMobileMenuOpen.value) {
      closeMobileMenu()
    }
  }
  window.addEventListener('resize', handleResize)
})

// ç»„ä»¶å¸è½½
onUnmounted(() => {
  window.removeEventListener('online', handleOnline)
  window.removeEventListener('offline', handleOffline)
  document.body.style.overflow = 'auto'
})
